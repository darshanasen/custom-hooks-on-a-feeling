// Example of repeating logic in regular ol' JS

const add = (a, b) => {
    const sum = a + b;
    if (sum > 10) {
        console.log('Whoa big number');
    } else {
        console.log('Super small');
    }
}

const subtract = (a, b) => {
    const difference = a - b;
    if (difference > 10) {
        console.log('Whoa big number');
    } else {
        console.log('Super small');
    }
}